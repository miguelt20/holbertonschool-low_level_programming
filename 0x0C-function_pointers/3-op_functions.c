#include "3-calc.h"

/**
 *op_add - sum two numbers
 *@a: first number
 *@b: second number
 *Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - substract two numbers
 *@a: first number
 *@b: second number
 *Return: the substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - multiplies two numbers
 *@a: first number
 *@b: second number
 *Return: the mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - divides two numbers
 *@a: first number
 *@b: second number
 *Return: the div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - remainder of the division
 *@a: first number
 *@b: second number
 *Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
